/*
Operadores lógicos
&& -> and - Todas as expressões precisam ser verdadeiras para retornar true
|| -> or - Só uma precisa estar verdadeira para retornar true
! -> not - 

*/


const expressao_and = true && true && true && true;
const expressao_or = false || true || false || false; 
const expressao_not = !false;
console.log(expressao_and);
console.log(expressao_or);
console.log(expressao_not);


